public class OperadoresAsignacion {

    public static void main(String[] args) {

        //ASIGNACIÃ“N PURA
        int a = 7;

        //ASIGNACIONES ABREVIADAS
        a += 4;//es lo mismo que a = a + 4
        a -= 4;//es lo mismo que a = a - 4
        a *= 4;//es lo mismo que a = a * 4
        a /= 4;//es lo mismo que a = a / 4
        a %= 4;//es lo mismo que a = a % 4

        System.out.println("a = " + a);

    }
}
