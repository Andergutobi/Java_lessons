public class OperadoresAritmeticos {


    public static void main(String[] args) {

        //SUMA
        double i = 7;
        double j = 2;

        double resultadoSuma = i + j;
        System.out.println("resultadoSuma = " + resultadoSuma);

        //RESTA
        double resultadoResta = i - j;
        System.out.println("resultadoResta = " + resultadoResta);

        //MULTIPLICACIÓN
        double resultadoMultiplicacion = i * j;
        System.out.println("resultadoMultiplicacion = " + resultadoMultiplicacion);

        //DIVISION
        double resultadoDivision = i / j;
        System.out.println("resultadoDivision = " + resultadoDivision);

        //RESTO O MÓDULO
        double resultadoRestoOModulo = i % j;
        System.out.println("resultadoRestoOModulo = " + resultadoRestoOModulo);


    }
}
